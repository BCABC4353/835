def is_no_surprises_act_claim(rarc_codes):
    """
    Detect if claim is subject to No Surprises Act provisions
    Args:
        rarc_codes: List of RARC codes from LQ segments
    Returns:
        dict with NSA status and metadata
    """
    nsa_alert_codes = {'N864', 'N865', 'N866'}
    nsa_all_codes = {
        'N862', 'N864', 'N865', 'N866', 'N867', 'N868', 'N869', 'N870',
        'N871', 'N872', 'N873', 'N874', 'N875', 'N876', 'N877', 'N878', 'N879'
    }
    rarc_set = set(rarc_codes)
    nsa_codes_present = rarc_set.intersection(nsa_all_codes)
    if not nsa_codes_present:
        return {'is_nsa_claim': False}
    claim_type = None
    if 'N864' in rarc_set:
        claim_type = 'Emergency Services'
    elif 'N865' in rarc_set:
        claim_type = 'Non-Emergency at Participating Facility'
    elif 'N866' in rarc_set:
        claim_type = 'Air Ambulance'
    payment_method = None
    if 'N869' in rarc_set:
        payment_method = 'QPA (Qualifying Payment Amount)'
    elif 'N871' in rarc_set or 'N872' in rarc_set:
        payment_method = 'State Law'
    elif 'N873' in rarc_set:
        payment_method = 'All-Payer Model'
    elif 'N874' in rarc_set:
        payment_method = 'Open Negotiation'
    elif 'N875' in rarc_set:
        payment_method = 'IDR Entity Decision'
    balance_billing_prohibited = 'N878' in rarc_set or 'N879' in rarc_set
    idr_eligible = claim_type is not None and 'N875' not in rarc_set
    return {
        'is_nsa_claim': True,
        'claim_type': claim_type,
        'payment_method': payment_method,
        'balance_billing_prohibited': balance_billing_prohibited,
        'idr_eligible': idr_eligible,
        'nsa_codes': list(nsa_codes_present),
        'idr_deadline_days': 30 if idr_eligible else None
    }


def is_qmb_patient(carc_codes, group_codes):
    """
    Detect if patient is Qualified Medicare Beneficiary (QMB)
    Args:
        carc_codes: List of CARC codes from CAS segments
        group_codes: List of corresponding group codes
    Returns:
        bool indicating QMB status
    """
    if '303' in carc_codes:
        return True
    qmb_indicator_carcs = {'1', '2', '3'}
    for i, carc in enumerate(carc_codes):
        if carc in qmb_indicator_carcs and i < len(group_codes):
            if group_codes[i] == 'CO':
                return True
    return False
